package de.hdm_stuttgart.mi.sda1.sql2catalog;

public class StudyCourse {

	String id;
	String name;
	String shortName;
	String program;
	
	
	public StudyCourse(String id, String name, String shortName, String program) {
		super();
		this.id = id;
		this.name = name;
		this.shortName = shortName;
		this.program = program;
	}

}
