package de.hdm_stuttgart.mi.sda1.sql2catalog;

public class Student {

	String id;
	String fullName;
	String email;
	String studyCourse;
	
	
	public Student(String id, String fullName, String email, String studyCourse) {
		super();
		this.id = id;
		this.fullName = fullName;
		this.email = email;
		this.studyCourse = studyCourse;
	}

	
	
	
}
